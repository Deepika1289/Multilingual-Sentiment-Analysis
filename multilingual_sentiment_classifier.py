# ── Dataset class ──
# PyTorch needs data in a specific format — we wrap our DataFrame in a Dataset class

class SentimentDataset(Dataset):
    """
    Wraps tokenized text + labels for PyTorch.
    __len__  → how many samples total
    __getitem__ → returns one sample (input_ids, attention_mask, label)
    """
    def __init__(self, texts, labels, tokenizer, max_length=128):
        self.encodings = tokenizer(
            list(texts),
            truncation=True,       # cut if longer than max_length
            padding=True,          # pad shorter sequences
            max_length=max_length,
            return_tensors='pt'    # return PyTorch tensors
        )
        self.labels = torch.tensor(list(labels), dtype=torch.long)

    def __len__(self):
        return len(self.labels)

    def __getitem__(self, idx):
        return {
            'input_ids':      self.encodings['input_ids'][idx],
            'attention_mask': self.encodings['attention_mask'][idx],
            'labels':         self.labels[idx]
        }

