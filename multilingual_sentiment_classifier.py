"""## 🏋️ Step 7 — Fine-tune the Model

### Theory: Training Arguments explained

| Argument | Value | Why |
|---|---|---|
| `num_train_epochs` | 3 | 3 passes over data — enough for fine-tuning |
| `per_device_train_batch_size` | 16 | 16 samples per gradient update |
| `learning_rate` | 2e-5 | Small LR — we don't want to overwrite pre-trained weights |
| `warmup_steps` | 100 | Gradually increase LR at start — stabilises training |
| `weight_decay` | 0.01 | L2 regularisation — prevents overfitting |
| `evaluation_strategy` | epoch | Evaluate on val set after each epoch |

### Why learning rate 2e-5?
Pre-trained models are already very good. A large LR would **catastrophically forget** what they learned.
2e-5 (0.00002) is small enough to adapt without forgetting.

### What is warmup?
At the start of training, the randomly-initialised classification head has random weights.
Warmup starts with a tiny LR and gradually increases it — prevents the head from causing wild updates early on.

"""

def compute_metrics(eval_pred):
    """Called after each epoch — returns accuracy and F1"""
    logits, labels = eval_pred
    predictions = np.argmax(logits, axis=1)  # pick highest probability class
    return {
        'accuracy': accuracy_score(labels, predictions),
        'f1':       f1_score(labels, predictions, average='weighted'),
    }

# ── Training arguments ──
training_args = TrainingArguments(
    output_dir              = './sentiment_model',
    num_train_epochs        = 3,
    per_device_train_batch_size = 16,
    per_device_eval_batch_size  = 32,
    learning_rate           = 2e-5,
    warmup_steps            = 100,
    weight_decay            = 0.01,
    eval_strategy           = 'epoch', # Changed from evaluation_strategy
    save_strategy           = 'epoch',
    load_best_model_at_end  = True,
    metric_for_best_model   = 'f1',
    logging_dir             = './logs',
    logging_steps           = 20,
    report_to               = 'none',   # disable wandb
    fp16                    = torch.cuda.is_available(),  # use half precision on GPU
)

# ── Trainer ──
trainer = Trainer(
    model           = model,
    args            = training_args,
    train_dataset   = train_dataset,
    eval_dataset    = val_dataset,
    compute_metrics = compute_metrics,
)

print("✅ Trainer ready!")
print(f"   Epochs         : {training_args.num_train_epochs}")
print(f"   Batch size     : {training_args.per_device_train_batch_size}")
print(f"   Learning rate  : {training_args.learning_rate}")
print(f"   Mixed precision: {training_args.fp16}")

# ── TRAIN ──
# ⏱️  CPU: ~10-15 minutes | GPU: ~2-3 minutes

print("🚀 Starting fine-tuning...")
print("   Watch the training loss go DOWN and val F1 go UP — that's learning happening!\n")

train_result = trainer.train()

print(f"\n✅ Training complete!")
print(f"   Total training time : {train_result.metrics['train_runtime']:.0f} seconds")
print(f"   Samples/second      : {train_result.metrics['train_samples_per_second']:.1f}")

# Save model
trainer.save_model('./sentiment_model_final')
tokenizer.save_pretrained('./sentiment_model_final')
print(f"   Model saved to: ./sentiment_model_final/")

"""## 📈 Step 8 — Evaluate on Test Set

### Metrics we care about:

| Metric | Formula | What it means |
|---|---|---|
| **Accuracy** | correct / total | Overall % correct |
| **Precision** | TP / (TP+FP) | Of predicted positives, how many are right? |
| **Recall** | TP / (TP+FN) | Of actual positives, how many did we catch? |
| **F1 Score** | 2 × (P×R)/(P+R) | Harmonic mean of precision & recall |

### Why F1 over accuracy?
If classes are imbalanced (more positive tweets than negative),
accuracy is misleading. F1 penalises models that ignore minority classes.

### What is the confusion matrix?
Shows where the model makes mistakes:
- Rows = actual labels
- Columns = predicted labels
- Diagonal = correct predictions

"""

# ── Predictions on test set ──
predictions_output = trainer.predict(test_dataset)
y_pred = np.argmax(predictions_output.predictions, axis=1)
y_true = test_dataset.labels.numpy()

label_names = ['Negative', 'Neutral', 'Positive']

