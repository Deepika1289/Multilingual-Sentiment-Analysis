"""## 🔤 Step 5 — Tokenization

### Theory: How does DistilBERT read text?

Computers don't understand words — they understand **numbers**. Tokenization converts text → numbers.

**Step-by-step:**
```
Input : "I love this product!"
   ↓  (tokenizer splits into subwords)
Tokens: ["I", "love", "this", "product", "!"]
   ↓  (each token gets an ID from the vocabulary)
IDs   : [101, 1045, 2293, 2023, 4031, 999, 102]
   ↓  (plus attention mask — 1 = real token, 0 = padding)
Mask  : [1,   1,    1,    1,    1,    1,   1  ]
```

### Special tokens:
- `[CLS]` (ID 101) — always first, carries the sentence meaning for classification
- `[SEP]` (ID 102) — marks end of sentence
- `[PAD]` — fills short sentences to match batch length

### Why `max_length=128`?
Tweets are short (~15-40 words). 128 tokens is more than enough and keeps training fast.

"""

