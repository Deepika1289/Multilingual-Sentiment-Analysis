"""## 🧠 Step 6 — Load DistilBERT Model

### Theory: What is Fine-tuning?

DistilBERT was **pre-trained** on Wikipedia + BookCorpus in 104 languages.
It learned general language understanding — grammar, context, word relationships.

**Fine-tuning** = take this pre-trained model and train it a little more on our specific task (sentiment).

Think of it like:
- Pre-training = going to school for 12 years (general knowledge)
- Fine-tuning = doing a 2-week course in your specific job (task-specific)

### Architecture: What happens inside?

```
Input Tokens
     ↓
DistilBERT (6 transformer layers — 66M parameters)
     ↓
[CLS] token embedding (768 dimensions)
     ↓
Dropout (prevents overfitting)
     ↓
Linear layer (768 → 3)
     ↓
Softmax → [P(Negative), P(Neutral), P(Positive)]
```

We only add the **last linear layer** — everything else is pre-trained.

"""

# ── Load DistilBERT with classification head ──
